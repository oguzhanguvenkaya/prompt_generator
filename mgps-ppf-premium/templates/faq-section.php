                <div class="mgps-faq-answer" id="faq-answer-1" role="region">
                    <p>
                        PPF (Paint Protection Film), aracınızın boya yüzeyini taş çarpması, çizik, böcek asidi, kuş pisliği, ağaç reçinesi ve UV ışınlarına karşı koruyan şeffaf poliüretan (TPU) filmdir. MG PS PPF serisi, 170–190 mikron kalınlığında, kendini yenileyen (self-healing) teknolojiye sahip premium koruma filmleri sunar.
                    </p>
                </div>
            </div>

            <!-- Soru 2 -->
