                <button class="mgps-faq-question" aria-expanded="false" aria-controls="faq-answer-1">
                    <span class="mgps-faq-q-text">PPF (Paint Protection Film) nedir ve ne işe yarar?</span>
                    <span class="mgps-faq-chevron" aria-hidden="true">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
                            <polyline points="6 9 12 15 18 9"/>
                        </svg>
                    </span>
