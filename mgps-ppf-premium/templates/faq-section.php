                    <span class="mgps-faq-q-text">Philic ve Phobic arasındaki fark nedir?</span>
                    <span class="mgps-faq-chevron" aria-hidden="true">
                        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
                            <polyline points="6 9 12 15 18 9"/>
                        </svg>
                    </span>
                </button>
                <div class="mgps-faq-answer" id="faq-answer-2" role="region">
                    <p>
                        <strong>Philic (Hidrofilik)</strong> yüzey, suyu yüzeyde ince bir tabaka halinde yayarak kolay temizlik sağlar. <strong>Phobic (Hidrofobik)</strong> yüzey ise suyu damlacıklar halinde iterek kir ve lekelerin yapışmasını önler. Phobic serisi ayrıca üstün kendini yenileme ve leke tutmama performansı sunar.
                    </p>
                </div>
            </div>

            <!-- Soru 3 -->
            <div class="mgps-faq-item">
                <button class="mgps-faq-question" aria-expanded="false" aria-controls="faq-answer-3">
